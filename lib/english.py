def scorefull(d):
    s = float(0)
    for x in d:
        s += scorechar(x)
    return s/len(d)

def findhigh( dp ):
    di = 0
    ds = -100.
    for i in range(len(dp)):
        s = scorefull( dp[i] )
        if s > ds:
            ds = s
            di = i
    return (ds,dp[di],di)

